package July22;

import java.util.Scanner;

public class ForEachLoop {
    public static void main(String[] args) {

        String names[] = new String[5];

        names[0] = "Monik";
        names[1] = "Krishna";
        names[2] = "Dhruvi";
        names[3] = "Heena";
        names[4] = "Rasik";


        for (String n:
             names) {
            System.out.println(n);
        }
        }

    }


